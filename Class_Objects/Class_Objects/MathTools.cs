using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Class_Objects
{
    class MathTools
    {
        public static int addSix (int num)
        {
            int sum = 6 + num;
            return sum;
        }

        public static int multiplyByEight (int num)
        {
            int sum = 8 * num;
            return sum;
        }

        public static int divideByTwo (int num)
        {
            int sum = num / 2;
            return sum;
        }
    }
